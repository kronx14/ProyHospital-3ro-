    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String buscado = txtBuscarPaciente.getText().trim();

        if (buscado.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Escribe un expediente.");
            return;
        }
      
        Paciente encontrado = listaPacientes.buscar(buscado);

        if (encontrado != null) {
            //  Si lo encontramos, llenamos la tabla con sus citas
            llenarTabla(encontrado);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Paciente no encontrado.");
            cargarTablaMain();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

