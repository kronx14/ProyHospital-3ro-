        //llenar la tabla con citas del paciente buscado
    public void llenarTabla(Paciente paciente) {
        DefaultTableModel modelo = (DefaultTableModel) tblHistorialCitas.getModel();
        modelo.setRowCount(0);
        
        // Obtener las citas del paciente
        Cita[] citas = paciente.getListaCitas();
        
        // Recorrer el arreglo y llenar el modelo
        if (citas != null) {
            for (Cita c : citas) {
                // Validamos que la cita no sea nula 
                if (c != null) {
                    Object[] fila = {
                        c.getFolio() != null ? c.getFolio() : "S/N", 
                        c.getNombre(),
                        c.getApellidos(),
                        c.getFecha(),
                        c.getHora(),
                        c.getAsunto(),
                        c.isEstadoCita() ? "Activa" : "Cancelada"
                    };
                    modelo.addRow(fila);
                }
            }
        }
       
        tblHistorialCitas.setModel(modelo);
    }
    
    private void cargarTablaMain() {
        DefaultTableModel modelo = (DefaultTableModel) tblHistorialCitas.getModel();

        modelo.setRowCount(0);
        Paciente actual = listaPacientes.cab; 

        while (actual != null) {
            Cita[] citas = actual.getListaCitas();

            if (citas != null) {
                //tomar cita de cada paciente
                for (Cita cita : citas) {
                    if (cita != null) {
                        // Agregar a la tabla
                        modelo.addRow(new Object[] {
                            cita.getFolio(),
                            cita.getNombre(),
                            cita.getApellidos(),
                            cita.getFecha(),
                            cita.getHora(),
                            cita.getAsunto(),
                            cita.isEstadoCita() ? "Activa" : "Cancelada"
                        });
                    }
                }
            }

            actual = actual.getLigaD();
        }
    }
    
    public Paciente buscarPaciente(String nombre, String apellidos) {
        Paciente actual = listaPacientes.cab;

        while (actual != null) {
            if (actual.getNombre().equalsIgnoreCase(nombre) && 
                actual.getApellidos().equalsIgnoreCase(apellidos)) {
                return actual;
            }
            actual = actual.getLigaD();
        }
        return null;
    }
    
    public void crearPaciente(String expediente, String nombre, String apellidos) {
        listaPacientes.insertLast(expediente, nombre, apellidos);
    }
    
    private void sacarCitas(Paciente paciente) {
        Cita[] citasActuales = paciente.getListaCitas();
        if (citasActuales == null) return;
        
        int contador = 0;
        for (Cita c : citasActuales) {
            if (c != null) {
                contador++;
            }
        }
        
        Cita[] citasNuevas = new Cita[contador];
        int index = 0;
        for (Cita c : citasActuales) {
            if (c != null) {
                citasNuevas[index++] = c;
            }
        }
        
        paciente.setListaCitas(citasNuevas);
    }
    
    private void eliminarCitaDePaciente(String folio) {
        Paciente actual = listaPacientes.cab;
        
        while (actual != null) {
            Cita[] citas = actual.getListaCitas();
            
            if (citas != null) {
                for (int i = 0; i < citas.length; i++) {
                    if (citas[i] != null && citas[i].getFolio() != null 
                        && citas[i].getFolio().equals(folio)) {
                        
                        citas[i] = null;
                        sacarCitas(actual);
                        return;
                    }
                }
            }
            
            actual = actual.getLigaD();
        }
    }
    
