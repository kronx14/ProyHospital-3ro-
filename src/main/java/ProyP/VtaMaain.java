    private void removeBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBttnActionPerformed
        if(tblHistorialCitas.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "Ningun elemento fue seleccionado", "ADVERTENCIA",
            JOptionPane.WARNING_MESSAGE);
            return;
        }
        String folio = tblHistorialCitas.getValueAt(tblHistorialCitas.getSelectedRow(), 0).toString();
        
        // Eliminar del grafo
        citas.eliminarX(folio);
        eliminarCitaDePaciente(folio);
        
        JOptionPane.showMessageDialog(this,
            "Paciente eliminado exitosamente",
            "Éxito",
            JOptionPane.INFORMATION_MESSAGE);
               
        cargarTablaMain();
    }//GEN-LAST:event_removeBttnActionPerformed

